    function isMobile() {
      return /Mobi|Android|iPhone|iPad/i.test(navigator.userAgent);
    }

    function smartCall(number) {
      if (isMobile()) {
        window.location.href = `tel:${number}`;
      } else {
        alert("This feature is for mobile devices. Please use a mobile phone to call.");
      }
    }

    function findHospitals() {
      const disease = document.getElementById("disease").value;
      const otherDisease = document.getElementById("otherDisease").value.trim();
      let query = disease || otherDisease;
      if (!query) {
        alert("тЪая╕П Please select or enter a condition first!");
        return;
      }
      const mapsUrl = `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(query + " hospital near me")}`;
      window.open(mapsUrl, "_blank");
    }

