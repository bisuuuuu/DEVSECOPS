    function setLanguage(lang) {
      document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
          element.innerHTML = translations[lang][key];
        }
      });
      // Also update placeholders and options that are not regular text
      document.getElementById('otherDisease').placeholder = translations[lang].label_or_enter;
      document.getElementById('medicine').placeholder = translations[lang].label_search_medicine_placeholder;
    }

    // Set initial language and add event listener
    document.addEventListener('DOMContentLoaded', () => {
      const languageSelector = document.getElementById('language-selector');
      setLanguage(languageSelector.value); // Set initial language
      languageSelector.addEventListener('change', (event) => {
        setLanguage(event.target.value);
      });
    });

